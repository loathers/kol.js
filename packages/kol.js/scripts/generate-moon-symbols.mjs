import { readFileSync, writeFileSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const moonsDir = join(__dirname, "../src/moons");
const outFile = join(__dirname, "../src/moonSymbols.ts");

const phases = [
  "new",
  "waxing-crescent",
  "first-quarter",
  "waxing-gibbous",
  "full",
  "waning-gibbous",
  "last-quarter",
  "waning-crescent",
];

function extractInner(svg) {
  return svg
    .replace(/<\?xml[^>]*\?>\s*/g, "")
    .replace(/<!--[\s\S]*?-->\s*/g, "")
    .replace(/<svg[^>]*>\s*/, "")
    .replace(/\s*<\/svg>\s*$/, "")
    .trim();
}

const entries = phases.map((phase) => {
  const raw = readFileSync(join(moonsDir, `moon-${phase}.svg`), "utf-8");
  const inner = extractInner(raw)
    .replace(/\\/g, "\\\\")
    .replace(/`/g, "\\`")
    .replace(/\$\{/g, "\\${");
  return `  // ${phase}\n  \`${inner}\``;
});

const output = `// Generated by scripts/generate-moon-symbols.mjs — edit the SVGs in src/moons/, then re-run the script.
const moonSymbols: string[] = [
${entries.join(",\n")}
];

export default moonSymbols;
`;

writeFileSync(outFile, output);
console.log(`Written ${outFile}`);
